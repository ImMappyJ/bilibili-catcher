function searchboxUp(){
    var searchbox = document.getElementById("form-searchbox");
    searchbox.style.animation = "up";
    searchbox.style.animationDuration = "0.5s";
    searchbox.style.padding = "10 0";
}

function searchboxDown(){
    var searchbox = document.getElementById("form-searchbox");
    searchbox.style.animation = "down";
    searchbox.style.animationDuration = "0.5s";
    searchbox.style.padding = "200 0";
}